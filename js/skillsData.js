// Define the typed answer variable
let typedAnswer;

function showAnswer(index, language) {
  const answerElement = document.querySelector(".answer");
  const translation = skillsData[index]?.answer[language];
  const typedSpeed = 0.2; // Adjust the typing speed as needed
  const direction = i18next.language === "ar" ? "rtl" : "ltr";

  // Destroy the previous typedAnswer instance if it exists
  if (typedAnswer) {
    typedAnswer.destroy();
  }

  answerElement.setAttribute("dir", direction);

  // Display the translated answer using typed.js
  typedAnswer = new Typed(answerElement, {
    strings: [translation || "Translation not available"],
    typeSpeed: typedSpeed,
    loop: false,
    showCursor: false,
  });
}
